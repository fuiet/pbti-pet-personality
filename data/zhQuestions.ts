export function localizeQuestions<T extends string>(questions: QuestionLike<T>[], useChinese: boolean): QuestionLike<T>[] {
  if (!useChinese) return questions;

  return questions.map((question) => {
    const copy = zhQuestionCopy[question.id];
    if (!copy) return question;
    return {
      ...question,
      question: copy.question,
      options: question.options.map((option, index) => ({ ...option, text: copy.options[index] || option.text })),
    };
  });
}
